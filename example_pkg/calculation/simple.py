def plus(x, y):
    """
    >>> plus(2,3)
    5
    >>> plus(1,2)
    3
    :param x: a float variable
    :param y: a float variable
    :return: the plus of two float variables
    """
    return x + y

def minus(x, y):
    '''
    >>> minus(2,3)
    -1
    >>> minus(3,1)
    2
    :param x: a float variable
    :param y: float variable
    :return: the minus of x and y
    '''
    return x - y

def multiply(x, y):
    '''
    >>>multiply(2, 3)
    6
    >>> multiply('a', 3)
    'aaa'
    :param x: a float variable
    :param y: a float variable
    :return: the multiply of x and y
    '''
    return x * y

